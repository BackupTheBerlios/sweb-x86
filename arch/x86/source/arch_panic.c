#include "arch_panic.h"

void arch_panic(char *mesg)
{
  uint8 * fb = (uint8*)0xC00B8000;
  unsigned i=0;
  while (mesg && *mesg)
  {
    fb[i++] = *mesg++;
    fb[i++] = 0x9f;
  }
  for (;;);
}
